#include "../Cub3D.h"

void	jsp(char **map)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (map[i])
	{
		while (map[i][j])
		{
			printf("%c ", map[i][j]);
			j++;
		}
		j = 0;
		printf("\n");
		i++;
	}
}
